void CubeMapEnvironmentMapping::Startup()
{

    // configure global opengl state
    // -----------------------------
    glEnable(GL_DEPTH_TEST);

    // build and compile our shader zprogram
    // ------------------------------------
    ShaderBuilder shader_builder("src/test_progs/learn_opengl/4.advanced_opengl/6.2.cubemaps_environment_mapping/");
    m_shader = shader_builder.Vert("6.2.cubemaps.vs").Frag("6.2.cubemaps.fs").Build("Cube map Env Map");
    m_skybox_shader = shader_builder.Vert("6.2.skybox.vs").Frag("6.2.skybox.fs").Build("Skybox");

    // cube VAO
    glGenVertexArrays(1, &cubeVAO);
    glGenBuffers(1, &cubeVBO);
    glBindVertexArray(cubeVAO);
    glBindBuffer(GL_ARRAY_BUFFER, cubeVBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(cubeVertices), &cubeVertices, GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(1);
    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)(3 * sizeof(float)));
    // skybox VAO
    glGenVertexArrays(1, &skyboxVAO);
    glGenBuffers(1, &skyboxVBO);
    glBindVertexArray(skyboxVAO);
    glBindBuffer(GL_ARRAY_BUFFER, skyboxVBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(skyboxVertices), &skyboxVertices, GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);

    // load textures
    // -------------
    std::vector<std::string> faces
    {
        std::string("assets/textures/skybox/right.jpg"),
        std::string("assets/textures/skybox/left.jpg"),
        std::string("assets/textures/skybox/top.jpg"),
        std::string("assets/textures/skybox/bottom.jpg"),
        std::string("assets/textures/skybox/front.jpg"),
        std::string("assets/textures/skybox/back.jpg")
    };
    cubemapTexture = loadCubemap(faces);

    // shader configuration
    // --------------------
    m_shader->Bind();
    m_shader->SetUniform1i("skybox", 0);

    m_skybox_shader->Bind();
    m_skybox_shader->SetUniform1i("skybox", 0);


    m_shader->OutputInfo();
    m_skybox_shader->OutputInfo();
}

void CubeMapEnvironmentMapping::Shutdown()
{
    glDeleteVertexArrays(1, &cubeVAO);
    glDeleteVertexArrays(1, &skyboxVAO);
    glDeleteBuffers(1, &cubeVBO);
    glDeleteBuffers(1, &skyboxVBO);
}

void CubeMapEnvironmentMapping::OnUpdate(double now, double time_step)
{
    m_shader->Bind();
    //transformations
    m_shader->SetUniformMat4f("projection", m_camera.ProjMatrix());
    m_shader->SetUniformMat4f("view", m_camera.ViewMatrix());
    m_shader->SetUniformMat4f("model", glm::mat4(1.0f));
    m_shader->SetUniform3f("cameraPos", m_camera.Position());
   
    // cubes
    glBindVertexArray(cubeVAO);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_CUBE_MAP, cubemapTexture);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);

    // draw skybox as last
    glDepthFunc(GL_LEQUAL);  // change depth function so depth test passes when values are equal to depth buffer's content
    m_skybox_shader->Bind();
    glm::mat4 view = glm::mat4(glm::mat3(m_camera.ViewMatrix())); // remove translation from the view matrix
    m_skybox_shader->SetUniformMat4f("view", view);
    m_skybox_shader->SetUniformMat4f("projection", m_camera.ProjMatrix());

    // skybox cube
    glBindVertexArray(skyboxVAO);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_CUBE_MAP, cubemapTexture);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);
    glDepthFunc(GL_LESS); // set depth function back to default
}

