void HDR::OnUpdate(double now, double time_step)
{
    bool* keys = m_window.GetKeys().m_key_code;
    hdr = keys[GLFW_KEY_H];

    // 1. render scene into floating point framebuffer
        // -----------------------------------------------
    glBindFramebuffer(GL_FRAMEBUFFER, hdrFBO);
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
    m_shader->Bind();
    m_shader->SetUniformMat4f("projection", m_camera.ProjMatrix());
    m_shader->SetUniformMat4f("view", m_camera.ViewMatrix());
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, woodTexture);
    // set lighting uniforms
    for (unsigned int i = 0; i < lightPositions.size(); i++)
    {
        m_shader->SetUniform3f("lights[" + std::to_string(i) + "].Position", lightPositions[i]);
        m_shader->SetUniform3f("lights[" + std::to_string(i) + "].Color", lightColors[i]);
    }
    m_shader->SetUniform3f("viewPos", m_camera.Position());
    // render tunnel
    glm::mat4 model = glm::mat4(1.0f);
    model = glm::translate(model, glm::vec3(0.0f, 0.0f, 25.0));
    model = glm::scale(model, glm::vec3(2.5f, 2.5f, 27.5f));
    m_shader->SetUniformMat4f("model", model);
    m_shader->SetUniform1i("inverse_normals", true);
    renderCube();
    glBindFramebuffer(GL_FRAMEBUFFER, 0);

    // 2. now render floating point color buffer to 2D quad and tonemap HDR colors to default framebuffer's (clamped) color range
    // --------------------------------------------------------------------------------------------------------------------------
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
    m_hdr_shader->Bind();
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, colorBuffer);
    m_hdr_shader->SetUniform1i("hdr", hdr);
    m_hdr_shader->SetUniform1f("exposure", exposure);
    renderQuad();

    std::cout << "hdr: " << (hdr ? "on" : "off") << "| exposure: " << exposure << std::endl;
}

